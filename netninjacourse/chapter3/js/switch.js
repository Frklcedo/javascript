// switch statement

const grade = 'A';

switch(grade){
    case 'A':
        console.log("You've got an A!!! Congratulations");
        break;
    case 'B':
        console.log("You've got an B!!");
        break;
    case 'C':
        console.log("You've got an C!");
        break;
    case 'D':
        console.log("You've got an D");
        break;
    case 'E':
        console.log("You've got an E");
        break;
    default:
        console.log("Not a valid grade.");
}